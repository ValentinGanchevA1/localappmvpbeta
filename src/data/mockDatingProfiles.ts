// Helper to generate random coordinates near a base location
const generateNearbyCoords = (baseLat: number, baseLng: number, radiusKm: number) => {
  const radiusInDegrees = radiusKm / 111; // Approx 111km per degree
  const randomLat = baseLat + (Math.random() - 0.5) * 2 * radiusInDegrees;
  const randomLng = baseLng + (Math.random() - 0.5) * 2 * radiusInDegrees;
  return {latitude: randomLat, longitude: randomLng};
};


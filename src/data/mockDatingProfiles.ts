// Function to get mock profiles filtered by user preferences
export const getMockDatingProfiles = (
  userGender?: string,
  lookingFor?: string,
  limit: number = 10
): DatingProfile[] => {
  let filtered = [...MOCK_DATING_PROFILES];

  // Filter by what the user is looking for
  if (lookingFor && lookingFor !== 'everyone') {
    filtered = filtered.filter(p => p.gender === lookingFor);
  }

  // Filter by who is looking for the user's gender
  if (userGender) {
    filtered = filtered.filter(p =>
      p.lookingFor === 'everyone' || p.lookingFor === userGender
    );
  }

  // Shuffle and limit
  return filtered
    .sort(() => Math.random() - 0.5)
    .slice(0, limit);
};

